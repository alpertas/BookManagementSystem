    public void filter(String query)
    {
        TableRowSorter<DefaultTableModel> trs =  new TableRowSorter<DefaultTableModel>(model);
        book_table.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(query));
    }
    
