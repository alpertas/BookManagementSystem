    private void btn_sepetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sepetActionPerformed
        String kAdet = tf_kitapAdet.getText();
        int yeniKAdet=Integer.parseInt(kAdet);
        
        String kitapStok1 = tf_kitapStok.getText();
        int yeniKitapS1 = Integer.parseInt(kitapStok1);
        
        if(yeniKAdet==0){
            JOptionPane.showMessageDialog(null, "Kitap Adeti En Az 1 Olmalıdır.");
        }else if(yeniKAdet>yeniKitapS1){
            JOptionPane.showMessageDialog(null, "Stokta istediğiniz kadar kitap bulunmamaktadır.");
        }
        else{
            String kAdi = lbl_kullaniciAdi.getText();
            String kitapAdi = tf_kitapAd.getText();
            String yazar = tf_kitapYazar.getText();
            String tip = tf_kitapTip.getText();
            String yayinEvi = tf_kitapYEvi.getText();
            String kitapFiyat = tf_kitapFiyat.getText();
            String kitapStok = tf_kitapStok.getText();
            int yeniKitapF = Integer.parseInt(kitapFiyat);
            int yeniKitapS = Integer.parseInt(kitapStok);
            String adet = tf_kitapAdet.getText();
            int yeniAdet=Integer.parseInt(adet);
            op.sepetAdd(kAdi,kitapAdi,yazar,tip,yayinEvi,yeniKitapF,yeniKitapS,yeniAdet);
            bookView();
        }
    }//GEN-LAST:event_btn_sepetActionPerformed

    public void bookView(){
        model.setRowCount(0);
        ArrayList<Book> books= new ArrayList<Book>();
        books=op.BookCome();
        
        if(books!=null)
        {
        
            for(Book bookx : books){
                Object[] add= {
                    bookx.getId(),bookx.getBook_name(),bookx.getBook_writer(),bookx.getBook_type(),bookx.getBook_publisher(),bookx.getBookshelf(),bookx.getShelf(),bookx.getFiyat(),bookx.getStok()
                };
                model.addRow(add);
            }
            
        }
    }
    
